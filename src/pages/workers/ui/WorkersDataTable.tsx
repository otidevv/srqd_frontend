// Create a separate component for the drag handle
function DragHandle({ id }: { id: string }) {
  const { attributes, listeners } = useSortable({
    id,
  })

  return (
    <Button
      {...attributes}
      {...listeners}
      variant="ghost"
      size="icon"
      className="text-muted-foreground size-7 hover:bg-transparent"
    >
      <IconGripVertical className="text-muted-foreground size-3" />
      <span className="sr-only">Drag to reorder</span>
    </Button>
  )
}

// Get badge variant for contract type
function getContractBadgeVariant(contractType: ContractType) {
  switch (contractType) {
    case ContractType.NOMBRADO:
      return "default"
    case ContractType.CAS:
      return "secondary"
    case ContractType.LOCACION:
      return "outline"
    case ContractType.PRACTICANTE:
      return "outline"
    default:
      return "outline"
  }
}

// Get badge variant for status
function getStatusBadgeVariant(status: WorkerStatus) {
  switch (status) {
    case WorkerStatus.ACTIVE:
      return "default"
    case WorkerStatus.VACATION:
      return "secondary"
    case WorkerStatus.INACTIVE:
      return "outline"
    case WorkerStatus.SUSPENDED:
      return "destructive"
    default:
      return "outline"
  }
}

